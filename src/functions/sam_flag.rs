// get & interpret
/// Binary interpretation on a SAM flag.
pub trait SAMFlag {
  fn interpret(
    &self,
    p: usize,
  ) -> bool {
    interpret(self.get_flag(), p)
  }

  fn get_flag(&self) -> i32;
}

