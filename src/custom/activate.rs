////////////////////////////////////////////////////////////////////////////////////////////////////

// activation trait
pub trait ActivateExt {
  fn activate(&mut self);
  fn deactivate(&mut self);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

// extend implement on boolean
impl ActivateExt for bool {
  fn activate(&mut self) {
    *self = true;
  }

  fn deactivate(&mut self) {
    *self = false;
  }
}

