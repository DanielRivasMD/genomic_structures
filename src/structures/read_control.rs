/// Memory containing struct about read ID.
#[derive(Debug, new, Default)]
pub struct ReadControl {
  // TODO: think about a way to implement read_id as &str
  #[new(default)]
  pub current: String,

  #[new(default)]
  pub previous: String,
}

////////////////////////////////////////////////////////////////////////////////////////////////////

// read memory
impl ReadControl {
  pub fn read_memory(&mut self) {
    self.previous = self.current.clone();
  }
}

