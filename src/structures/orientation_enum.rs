////////////////////////////////////////////////////////////////////////////////////////////////////

/// Orientation pair.
pub struct OrientationPair(pub usize, pub usize);

////////////////////////////////////////////////////////////////////////////////////////////////////

/// Annotate orientation.
#[derive(Debug, new, Clone, PartialEq)]
pub enum OrientationEnum {
  Downstream,
  Upstream,
  Palindromic,
  None,
}

////////////////////////////////////////////////////////////////////////////////////////////////////

// manual default trait implementation
impl Default for OrientationEnum {
  fn default() -> Self {
    Self::None
  }
}

